<?php

namespace App;

use Illuminate\Database\Eloquent\Model;
use DB;
use App\User;
use App\Stock;

class Auction extends Model
{
    public $table = 'auctions';
    public $fillable = ['title','description','date_start','date_end','stock_id','created_at','updated_at'];

    public function getWinner($id){
        $winner = DB::table('auction_has_winner')->where('auction_id',$id)->get();
        if(!empty($winner)){
            if($winner[0]->bidding_id != NULL){
                $id_winner = DB::table('biddings')->where('id',$winner[0]->bidding_id)->get();
                $winner_data = User::where('id',$id_winner[0]->user_id)->get();
            }else{
               $winner_data = NULL; 
            }
        }else{
            $winner_data = NULL;
        }
        return $winner_data[0];
    }

    public function getProduct($id){
        $stock = DB::table('stock')->where('id',$id)->get(['product_id']);
        $product = DB::table('products')->where('id',$stock[0]->product_id)->get();
        //dd($product[0]);
        return $product[0];
    }

    public function getCategory($stock_id,$id){
        $product = $this->getProduct($stock_id);
        $categories = DB::table('product_has_category')->where('product_id',$product->id)->where('category_id',$id)->get();
        if(sizeof($categories) == 0){
            return null;
        }
        $category = Category::where('id',$categories[0]->category_id)->get();
        //dd($category);
        return $category[0];
    }
}
























































































































































































































































































































































































































































































































































































































































































































































































































































































































